#[test]
fn blocked_receipt_withholds_claims() {
    let receipt = serde_json::json!({
        "coverage": {
            "policy": "blocked_or_withheld",
            "percent": 0,
            "floor_percent": 100,
            "owner": "root",
            "reason": "No coverage command configured.",
            "blocker_or_debt_id": "coverage-command-not-configured"
        },
        "claim_ceiling": "withheld_or_blocked",
        "uncovered_records": []
    });
    assert!(
        validate_coverage_claim("blocked_or_withheld", "withheld_or_blocked", &receipt).is_ok()
    );
}
#[test]
fn full_coverage_requires_empty_uncovered_records_and_complete_ceiling() {
    let ok = serde_json::json!({
        "coverage": {"percent": 100.0, "floor_percent": 100.0},
        "claim_ceiling": "supports_complete_claim",
        "uncovered_records": []
    });
    assert!(
        validate_coverage_claim("100_percent_required", "supports_complete_claim", &ok).is_ok()
    );
    let uncovered = serde_json::json!({
        "coverage": {"percent": 100.0, "floor_percent": 100.0},
        "claim_ceiling": "supports_complete_claim",
        "uncovered_records": [{"path": "src/main.rs"}]
    });
    assert!(
        validate_coverage_claim(
            "100_percent_required",
            "supports_complete_claim",
            &uncovered
        )
        .is_err()
    );
}
#[test]
fn coverage_exclusions_must_be_reviewed_and_non_covering() {
    let ok = serde_json::json!({
        "exclusions": [{
            "path": "generated/client.rs",
            "kind": "generated",
            "rationale": "generated by upstream schema",
            "counts_as_covered": false,
            "reviewed": true
        }]
    });
    assert!(validate_exclusions(&ok).is_ok());
    let bad = serde_json::json!({
        "exclusions": [{
            "path": "src/live.rs",
            "kind": "runtime",
            "rationale": "hard to test",
            "counts_as_covered": true,
            "reviewed": true
        }]
    });
    assert!(validate_exclusions(&bad).is_err());
}
#[test]
fn require_str_and_array_report_shape_errors() {
    let value = serde_json::json!({"name": "Narrated", "items": ["x"]});

    assert_eq!(
        require_str(&value, "/name", "value", None).unwrap(),
        "Narrated"
    );
    assert!(require_str(&value, "/missing", "value", None).is_err());
    assert_eq!(require_nonempty_array(&value, "/items").unwrap().len(), 1);
    assert!(require_nonempty_array(&serde_json::json!({"items":[]}), "/items").is_err());
}

